package org.yawlfoundation.yawl.dspy.adaptation;

/**
 * Skip a task in the workflow due to changed conditions.
 *
 * @param taskId task identifier to skip
 * @param reason explanation for skipping
 */
public record SkipTask(String taskId, String reason) implements AdaptationAction {
    @Override
    public String reasoning() {
        return "Skip task " + taskId + ": " + reason;
    }
}

/**
 * API Gateway integration patterns for YAWL v6.0.0 (Kong, Traefik, AWS API Gateway).
 *
 * <p>This package provides configuration models, route definitions, and runtime
 * integration adapters for deploying YAWL behind enterprise API gateways. Three
 * gateway targets are supported with idiomatic configurations:
 *
 * <ul>
 *   <li><b>Kong Gateway</b> - declarative {@code kong.yaml} route and plugin
 *       configuration generated by {@link org.yawlfoundation.yawl.integration.gateway.kong.KongConfigurationGenerator}.
 *       Plugins configured: rate-limiting, jwt, request-transformer, response-transformer,
 *       correlation-id, prometheus, circuit-breaker (via Kong Enterprise or community plugins).</li>
 *   <li><b>Traefik</b> - dynamic YAML/TOML provider rules generated by
 *       {@link org.yawlfoundation.yawl.integration.gateway.traefik.TraefikConfigurationGenerator}.
 *       Middlewares: stripPrefix, rateLimit, basicAuth/forwardAuth (OIDC),
 *       retry, circuitBreaker, headers.</li>
 *   <li><b>AWS API Gateway</b> - OpenAPI 3.0 with AWS extensions ({@code x-amazon-apigateway-*})
 *       generated by {@link org.yawlfoundation.yawl.integration.gateway.aws.AwsApiGatewayConfigGenerator}.
 *       Lambda authorizer, usage plans, throttling, and VPC link integration.</li>
 * </ul>
 *
 * <h2>Rate Limiting Strategy</h2>
 * <p>YAWL API endpoints are grouped into three tiers:
 * <ul>
 *   <li><b>Critical</b> (case launch, spec load): 10 req/min per client</li>
 *   <li><b>Standard</b> (work item operations): 100 req/min per client</li>
 *   <li><b>Read-only</b> (status queries, list ops): 600 req/min per client</li>
 * </ul>
 *
 * <h2>Circuit Breaker Integration</h2>
 * <p>{@link org.yawlfoundation.yawl.integration.gateway.GatewayCircuitBreakerConfig}
 * defines gateway-level circuit breaker thresholds that complement the engine-side
 * {@link org.yawlfoundation.yawl.integration.autonomous.resilience.CircuitBreaker}.
 * Gateway breakers protect upstream services from YAWL overload; engine breakers
 * protect YAWL from downstream service failures.
 *
 * @author YAWL Foundation
 * @version 6.0.0
 * @since 6.0.0
 */
package org.yawlfoundation.yawl.integration.gateway;

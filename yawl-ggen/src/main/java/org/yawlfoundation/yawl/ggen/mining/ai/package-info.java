/**
 * AI-powered validation and optimization for process mining using modern
 * Large Language Model integration.
 *
 * <p>This package implements intelligent validation loops and automated
 * optimization strategies for process mining workflows.</p>
 *
 * <h2>LLM Validation Loop</h2>
 * <p>Provides a comprehensive validation framework that uses LLMs to:
 *   <ul>
 *     <li>Validate discovered process models for correctness and completeness</li>
 *     <li>Suggest optimal Petri net structures based on best practices</li>
 *     <li>Detect and resolve conflicts in process models</li>
 *     <li>Generate natural language explanations for model decisions</li>
 *     <li>Perform semantic analysis of process models using embedded knowledge</li>
 *   </ul>
 * </p>
 *
 * <h2>Metrics and Analysis</h2>
 * <p>Intelligent metrics collection and analysis using AI-powered insights:
 *   <ul>
 *     <li>Process quality metrics with AI-based scoring</li>
 *     <li>Anomaly detection using pattern recognition</li>
 *     <li>Performance prediction and bottleneck identification</li>
 *     <li>Process improvement recommendations</li>
 *     <li>Automated documentation generation</li>
 *   </ul>
 * </p>
 *
 * <h2>Java 25 Integration</h2>
 * <p>Leverages modern Java 25 features for enhanced AI capabilities:
 *   <ul>
 *     <li>Virtual threads for concurrent LLM API calls</li>
 *     <li>Sequenced collections for structured response processing</li>
 *     <li>Pattern matching for response analysis</li>
 *     <li>Records for immutable metric data structures</li>
 *     <li>Stream APIs with enhanced collectors for AI data processing</li>
 *   </ul>
 * </p>
 *
 * <h2>Key Components</h2>
 * <ul>
 *   <li>LLMClient - Integration with various LLM providers</li>
 *   <li>ValidationEngine - Core validation logic with AI feedback</li>
 *   <li>MetricsAnalyzer - Intelligent metrics collection and analysis</li>
 *   <li>ProcessOptimizer - AI-driven process model optimization</li>
 *   <li>KnowledgeBase - Embedded process mining expertise</li>
 * </ul>
 *
 * @since 6.0.0-GA
 * @author YAWL Foundation
 * @version 1.0
 */
package org.yawlfoundation.yawl.ggen.mining.ai;